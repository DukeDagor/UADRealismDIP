        public Ship.Store GetRandomShip(Player player, ShipType type, int desiredYear)
        {
            LoadPredefSets(prewarm: false, update: true);
            string name = player.data.name;
            if (desiredYear < Config.StartingYear)
            {
                _ = Config.StartingYear;
            }

            PlayerTechData playerTechData = _playerTechData[player.data.name];
            for (int i = 0; i < _predefData.Count; i++)
            {
                PredefData predefData = _predefData[i];
                if (!predefData.loadedData
                    || predefData.skipChance > UnityEngine.Random.value
                    || !predefData.yearsPerPlayer.ContainsKey(name))
                {
                    continue;
                }

                string dataName = (_lastValidData == i) ? name : $"{i}_{name}";
                List<Ship.Store> designList = new List<Ship.Store>(10);
                int designsChecked = 0;
                int yearStart = 0;
                int yearEnd = 0;
                int year = playerTechData.maxTechLevel;

                while (year > playerTechData.minTechLevel - 10 && designList.Count < 10)
                {
                    if (!CampaignController.Instance._currentDesigns.shipsPerYear.TryGetValue(year, out var shipsPerYear)
                        || !shipsPerYear.shipsPerPlayer.TryGetValue(dataName, out var shipsPerPlayer))
                        continue;

                    if (yearStart == 0)
                        yearStart = year;

                    yearEnd = year;

                    if (!shipsPerPlayer.shipsPerType.TryGetValue(type.name, out var shipsPerType))
                        continue;

                    foreach (var design in shipsPerType)
                    {
                        designsChecked++;
                                
                        if (CampaignControllerM.TechMatchRatio(design) >= 0f)
                            continue;

                        designList.Add(design);

                        if (designList.Count >= 10)
                            break;
                    }

                    year--;
                }

                Melon<TweaksAndFixes>.Logger.Msg($"Found {designList.Count}/10 options out of {designsChecked} designs checked in year {desiredYear} with tech range {yearStart} to {yearEnd}.");
                
                if (designList.Count != 0)
                {
                    return designList[0];
                }
            }
            return null;
        }
